<template>
  <a-layout class="layout">
    <a-layout-sider
      v-model:collapsed="collapsed"
      :trigger="null"
      collapsible
      width="200"
      theme="dark"
    >
      <div class="logo">
        <h2 v-if="!collapsed">政府管理员</h2>
        <h2 v-else>政府</h2>
      </div>
      <a-menu
        v-model:selectedKeys="selectedKeys"
        theme="dark"
        mode="inline"
      >
        <a-menu-item key="demands" @click="navigateTo('demands')">
          <template #icon>
            <FileSearchOutlined />
          </template>
          <span>需求审核</span>
        </a-menu-item>
        <a-menu-item key="overview" @click="navigateTo('overview')">
          <template #icon>
            <DashboardOutlined />
          </template>
          <span>项目总览</span>
        </a-menu-item>
        <a-menu-item key="reports" @click="navigateTo('reports')">
          <template #icon>
            <FileTextOutlined />
          </template>
          <span>报告中心</span>
        </a-menu-item>
      </a-menu>
    </a-layout-sider>
    
    <a-layout>
      <a-layout-header class="header">
        <div class="header-left">
          <menu-unfold-outlined
            v-if="collapsed"
            class="trigger"
            @click="() => (collapsed = !collapsed)"
          />
          <menu-fold-outlined
            v-else
            class="trigger"
            @click="() => (collapsed = !collapsed)"
          />
        </div>
        <div class="header-right">
          <a-dropdown>
            <a class="ant-dropdown-link" @click.prevent>
              <UserOutlined />
              {{ username }}
              <DownOutlined />
            </a>
            <template #overlay>
              <a-menu>
                <a-menu-item @click="logout">退出登录</a-menu-item>
              </a-menu>
            </template>
          </a-dropdown>
        </div>
      </a-layout-header>
      
      <a-layout-content class="content">
        <router-view />
      </a-layout-content>
    </a-layout>
  </a-layout>
</template>

<script setup>
import { ref, computed } from 'vue'
import { useRouter, useRoute } from 'vue-router'
import { 
  FileSearchOutlined, 
  DashboardOutlined, 
  FileTextOutlined,
  UserOutlined, 
  DownOutlined,
  MenuUnfoldOutlined,
  MenuFoldOutlined
} from '@ant-design/icons-vue'
import { message } from 'ant-design-vue'

const router = useRouter()
const route = useRoute()
const selectedKeys = ref([route.name?.replace('Government', '').toLowerCase()])
const collapsed = ref(false)

const username = computed(() => localStorage.getItem('username') || '政府管理员')

const navigateTo = (page) => {
  router.push(`/government/${page}`)
}

const logout = () => {
  // 检查是否为演示模式
  const demoMode = localStorage.getItem('demo_mode') === 'true'
  
  if (demoMode) {
    // 演示模式：清除演示数据
    localStorage.removeItem('demo_mode')
    localStorage.removeItem('demo_role')
    localStorage.removeItem('demo_user')
    message.success('已退出演示模式')
    // 使用 router.replace 确保路由正确跳转
    router.replace('/login')
  } else {
    // 真实模式：清除用户相关的本地存储
    localStorage.removeItem('current_user')
    localStorage.removeItem('user_role')
    message.success('已退出登录')
    // 使用 router.replace 确保路由正确跳转
    router.replace('/login')
  }
}
</script>

